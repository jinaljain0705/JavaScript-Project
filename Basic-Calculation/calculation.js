let X = 50, Y = 10;

let add = X + Y;
let sub = X - Y;
let mul = X * Y;
let div = X / Y;
let mod = X % Y;

console.log("===== Basic Calculation =====");

console.log("Value of X:", X);
console.log("Value of Y:", Y);

console.log("------------------------------");

console.log("1. Addition (X + Y) :", X + " + " + Y + " = " + add);
console.log("2. subtraction (X - Y) :", X + " - " + Y + " = " + sub);
console.log("3. Multiplication (X * Y) :", X + " * " + Y + " = " + mul);
console.log("4. Division (X / Y) :", X + " / " + Y + " = " + div);
console.log("5. Modulus (X % Y) :", X + " % " + Y + " = " + mod);
